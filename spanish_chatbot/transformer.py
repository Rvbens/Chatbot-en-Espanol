class Embedder(nn.Module):
    def __init__(self, vocab_size, d_model):
        super().__init__()
        self.d_model = d_model
        self.embed = nn.Embedding(vocab_size, d_model)
    def forward(self, x):
        return self.embed(x)

class PositionalEncoder(nn.Module):
    def __init__(self, d_model, max_seq_len = 200, dropout = 0.1):
        super().__init__()
        self.d_model = d_model
        self.dropout = nn.Dropout(dropout)
        # create constant 'pe' matrix with values dependant on 
        # pos and i
        pe = torch.zeros(max_seq_len, d_model, requires_grad=False)
        for pos in range(max_seq_len):
            for i in range(0, d_model, 2):
                pe[pos, i] = math.sin(pos / (10000 ** ((2 * i)/d_model)))
                pe[pos, i + 1] = math.cos(pos / (10000 ** ((2 * (i + 1))/d_model)))
        pe = pe.unsqueeze(0)
        self.register_buffer('pe', pe)
 
    
    def forward(self, x):
        # make embeddings relatively larger
        x = x * math.sqrt(self.d_model)
        #add constant to embedding
        seq_len = x.size(1)
        pe = self.pe[:,:seq_len].clone().detach()
        if x.is_cuda:
            pe.cuda()
        x = x + pe
        return self.dropout(x)
    
def nopeak_mask(size):
    np_mask = np.triu(np.ones((1, size, size)),k=1).astype('uint8')
    np_mask =  (torch.from_numpy(np_mask) == 0)
    return np_mask

def create_masks(input_seq, target_seq): #(bs,sl)
    # creates mask with 0s wherever there is padding in the input
    input_msk = (input_seq != PAD_token).unsqueeze(1) #(bs,1, sl)

    # create mask as before
    target_pad = PAD_token
    target_msk = (target_seq != PAD_token).unsqueeze(1) #(bs,1, sl)
    size = target_seq.size(1) # get seq_len for matrix
    no_peak_mask = nopeak_mask(size)
    if target_msk.is_cuda: no_peak_mask=no_peak_mask.cuda()
    target_msk = target_msk & no_peak_mask #(bs, sl, sl)
    return input_msk, target_msk

